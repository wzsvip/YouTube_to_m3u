import requests
import os
import sys

windows = False
if 'win' in sys.platform:
    windows = True

def get_url_content(url):
    headers = {
        "User-Agent": "Mozilla/5.0 (Linux; Android 8.0.0; Pixel 2 XL Build/OPD1.170816.004) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/83.0.4103.97 Mobile Safari/537.36"
    }
    response = requests.get(url, headers=headers)
    content = response.text
    return content
    
def grab(url):
    response = requests.get(url, timeout=15).text
    if '.m3u8' not in response:
        if '.m3u8' not in response:
            if windows:
                print('https://raw.githubusercontent.com/Kentsonshum/YouTube_to_m3u/main/assets/info.m3u')
                return
            os.system(f'curl "{url}" > temp.txt')
            response = ''.join(open('temp.txt').readlines())
            if '.m3u8' not in response:
                print('https://raw.githubusercontent.com/Kentsonshum/YouTube_to_m3u/main/assets/info.m3u')
                return
    end = response.find('.m3u8') + 5
    tuner = 100
    while True:
        if 'https://' in response[end-tuner : end]:
            link = response[end-tuner : end]
            start = link.find('https://')
            end = link.find('.m3u8') + 5
            break
        else:
            tuner += 5
    print(f"{link[start : end]}")

